<script type="module">
    document.addEventListener("DOMContentLoaded", () => {
        const r = document.querySelectorAll("#process-accordion .process-item");
        r.forEach((e) => {
            const t = e.querySelector(".process-toggle"),
                o = e.querySelector(".process-content"),
                s = e.querySelector(".vertical-line");
            t.addEventListener("click", () => {
                const i = t.getAttribute("aria-expanded") === "true";
                (r.forEach((c) => {
                        const a = c.querySelector(".process-toggle"),
                            n = c.querySelector(".process-content"),
                            l = c.querySelector(".vertical-line");
                        (a.setAttribute("aria-expanded", "false"),
                            (n.style.maxHeight = "0"),
                            (n.style.opacity = "0"),
                            l && (l.style.display = "block"));
                    }),
                    i ||
                    (t.setAttribute("aria-expanded", "true"),
                        (o.style.maxHeight = o.scrollHeight + "px"),
                        (o.style.opacity = "1"),
                        s && (s.style.display = "none")));
            });
        });
    });
</script>