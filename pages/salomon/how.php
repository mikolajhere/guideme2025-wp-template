<script>
    document.addEventListener("DOMContentLoaded", () => {
        const processItems = document.querySelectorAll(
            "#process-accordion .process-item"
        );

        processItems.forEach((item) => {
            const toggle = item.querySelector(".process-toggle");
            const content = item.querySelector(".process-content");
            const verticalLine = item.querySelector(".vertical-line");

            toggle.addEventListener("click", () => {
                const isOpen = toggle.getAttribute("aria-expanded") === "true";

                // Close all items
                processItems.forEach((otherItem) => {
                    const otherToggle = otherItem.querySelector(
                        ".process-toggle"
                    );
                    const otherContent = otherItem.querySelector(
                        ".process-content"
                    );
                    const otherLine = otherItem.querySelector(
                        ".vertical-line"
                    );
                    otherToggle.setAttribute("aria-expanded", "false");
                    otherContent.style.maxHeight = "0";
                    otherContent.style.opacity = "0";
                    otherContent.style.marginBottom = "0";
                    if (otherLine) otherLine.style.display = "block";
                });

                if (!isOpen) {
                    toggle.setAttribute("aria-expanded", "true");
                    content.style.maxHeight = content.scrollHeight + "px";
                    content.style.opacity = "1";
                    content.style.marginBottom = "1rem";
                    if (verticalLine) verticalLine.style.display = "none";
                }
            });
        });
    });
</script>